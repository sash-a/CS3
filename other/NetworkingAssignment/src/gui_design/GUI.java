    private void btn_groupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_groupActionPerformed
        String groupName = JOptionPane.showInputDialog("Enter group name");
        if (groupName == null || groupName.isEmpty())
        {
            return;
        }

        List<String> groupMembers = list_members.getSelectedValuesList();
        String message = "";
        for (String groupMember : groupMembers)
        {
            // Just need user ID
            message += groupMember.split(":")[0] + ":";
        }
        message += client.id;

        client.sendToServer("ginfo:-1:" + groupName, message);
    }//GEN-LAST:event_btn_groupActionPerformed

    long lastClicked = 0;
    static int doubleClickTime = 260;
    private void list_membersMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_list_membersMouseClicked
        if (System.currentTimeMillis() - lastClicked < doubleClickTime)
        {
            try
            {
                int otherID = (Integer.parseInt(list_members.getSelectedValue().split(":")[0]));
                ChatRoom chat = new ChatRoom(client, otherID, false);
                chat.setVisible(true);
                chat.setTitle(knownClients.get(otherID));
            } catch (Exception e)
            {
                JOptionPane.showMessageDialog(rootPane, "Did not select user");
            }
        }
        lastClicked = System.currentTimeMillis();
    }//GEN-LAST:event_list_membersMouseClicked

    private void list_groupsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_list_groupsMouseClicked
        if (System.currentTimeMillis() - lastClicked < doubleClickTime)
        {
            String selectedGroup = list_groups.getSelectedValue();
            if (selectedGroup == null)
            {
                return;
            }
            int groupID = Integer.parseInt(selectedGroup.split(":")[0]);
            client.addGroupChatRoom(groupID, selectedGroup.split(":")[1]);
        }
        lastClicked = System.currentTimeMillis();    }//GEN-LAST:event_list_groupsMouseClicked

