    public void addClient(int ID, String name)
    {
        if (client.id != ID)
        {
            knownClients.put(ID, name);
        }
        updateList();
    }

    public void updateList()
    {
        DefaultListModel<String> dlm = new DefaultListModel<>();
        for (int id : knownClients.keySet())
        {
            dlm.addElement(id + ":" + knownClients.get(id));
        }
        list_members.setModel(dlm);

        dlm = new DefaultListModel<>();
        for (int id : knownGroups.keySet())
        {
            dlm.addElement(id + ":" + knownGroups.get(id));
        }
        list_groups.setModel(dlm);
    }

